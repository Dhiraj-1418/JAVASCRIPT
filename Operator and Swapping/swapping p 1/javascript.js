// WAP of swaping of two variables using third variable


let a=prompt("Enter the value of a");
let b=prompt("Enter the value of b");

document.write("In first the value of a is = ",a);
document.write("\nIn first the value of b is = ",b);

let temp=a;
 a=b;
 b=temp;

document.write("\nAfter swapping the value of a is = ",a);
document.write("\nAfter swapping In first the value of b is = ",b);

